# código para calcular média e desvio padrão de uma lista

def calcular_media_desvio(lista):
    n = len(lista)
    if n == 0:
        return 0, 0
    media = sum(lista) / n
    variancia = sum((x - media) ** 2 for x in lista) / n
    desvio_padrao = variancia ** 0.5
    return media, desvio_padrao

